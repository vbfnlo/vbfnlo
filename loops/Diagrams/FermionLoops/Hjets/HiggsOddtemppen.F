       subroutine gluonpenHiggsOdd(m,p1,p2,p3,p4,p5,mup1,mup2,mup3,mup
     -   4,result,comp)
c ************************************************************************************
c determine the pentagon of
c p1(gluon)    ---->------->--------->--------->---   p5(B) p3^2=M_h
c                $         $          $              
c                $         $          $              
c                $         $          $              
c                V         V          V              
c                $         $          $              
c                $         $          $              
c               p2         p3         p4             
c              mu_p2                 mu_p4           
c              p2^2=0     p3^2=0    p4^2=0           
c Note: No couplings are assigned
c Note: To make it shorter in the promgram: mu_p2,...->mup2,... 
c Notation of External momenta: p1+p2+p3+p4+p5=0 
c mu_p1,mu_p2,mu_p3,mu_p4 should be think as external current 
c musq is the renormalization scale energy  
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c    Declaration of variables 
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
       IMPLICIT NONE
      Real*8   p1(0:3),p2(0:3),p3(0:3),p4(0:3),p5(0:3)
       Real*8   p1sq, p1p2, p1p3, p1p4, p1p5, p2sq, p2p3, p2p4, 
     -          p2p5, p3sq, p3p4, p3p5, p4sq, p4p5, p5sq
       Real*8   s12, s13, s14, s15, s23, s24, s25, s34, s35, s45
       Complex*16   p1mup1, p1mup2, p1mup3, p1mup4, p2mup1, p2mup2, 
     -          p2mup3, p2mup4, p3mup1, p3mup2, p3mup3, p3mup4, 
     -          p4mup1, p4mup2, p4mup3, p4mup4, p5mup1, p5mup2, 
     -          p5mup3, p5mup4
       Complex*16   mup1mup2, mup1mup3, mup1mup4, mup2mup3, mup2mup4, 
     -          mup3mup4
       Real*8 dotrr
       Complex*16 B0tM,C0fin1M,D04,E01M
       EXTERNAL dotrr,B0tM,C0fin1M,D04,E01M
       Complex*16 B012,B013,B014,B015,B023,B024,B025,B034,B035,B045
      Real*8 B012R,B013R,B014R,B015R,B023R,B024R,B025R,B034R,B035R,B045R
      Real*8 B012I,B013I,B014I,B015I,B023I,B024I,B025I,B034I,B035I,B045I
       Complex*16 C0123,C0124,C0125,C0134,C0135,C0145,C0234,C0235,C024
     -   5,C0345
       Real*8 C0123R,C0124R,C0125R,C0134R,C0135R,C0145R,C0234R,C0235R,
     -   C0245R,C0345R
       Real*8 C0123I,C0124I,C0125I,C0134I,C0135I,C0145I,C0234I,C0235I,
     -   C0245I,C0345I
       Real*8 C123R(4,2),C124R(4,2),C125R(4,2),C134R(4,2),C135R(4,2),C
     -   145R(4,2),C234R(4,2),C235R(4,2),C245R(4,2),C345R(4,2)
       Real*8 C123I(4,2),C124I(4,2),C125I(4,2),C134I(4,2),C135I(4,2),C
     -   145I(4,2),C234I(4,2),C235I(4,2),C245I(4,2),C345I(4,2)
       Complex*16 D01234,D01235,D01245,D01345,D02345,E012345
        Real*8 D01234R,D01235R,D01245R,D01345R,D02345R,E012345R
       Real*8 D01234I,D01235I,D01245I,D01345I,D02345I,E012345I
       Real*8 D1234R(13,3),D1235R(13,3),D1245R(13,3),D1345R(13,3),D234
     -   5R(13,3),E12345R(46,4)
        Real*8 D1234I(13,3),D1235I(13,3),D1245I(13,3),D1345I(13,3),D234
     -   5I(13,3),E12345I(46,4)
      Complex*16 F(138)
      Real*8 FI(138),FR(138),P(28)
      Complex*16 mup1(0:3),mup2(0:3),mup3(0:3),mup4(0:3)
      Complex*16 dotrc,dotcc,EPSCCCC,EPSCCCR,EPSCCRR,EPSCRRR,result
       Real*8 p5t,m,msq,EPSRRRR
      EXTERNAL   dotrc,dotcc,EPSCCCC,EPSCCCR,EPSCCRR,EPSCRRR,EPSRRRR
      Integer alpha
      Save F
      integer comp
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c       Definition of the scalar products. Not inlcueded the contraction of the
c       moments with the external currents  
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
       msq=m*m
       p1sq = dotrr(p1,p1)
       p1p2 = dotrr(p1,p2)
       p1p3 = dotrr(p1,p3)
       p1p4 = dotrr(p1,p4)
       p1p5 = dotrr(p1,p5)
       p2sq = dotrr(p2,p2)
       p2p3 = dotrr(p2,p3)
       p2p4 = dotrr(p2,p4)
       p2p5 = dotrr(p2,p5)
       p3sq = dotrr(p3,p3)
       p3p4 = dotrr(p3,p4)
       p3p5 = dotrr(p3,p5)
       p4sq = dotrr(p4,p4)
       p4p5 = dotrr(p4,p5)
       p5sq = dotrr(p5,p5)
       s12 = (p1sq +p2sq+ 2*p1p2) 
       s13 = (p1sq +p3sq+ 2*p1p3) 
       s14 = (p1sq +p4sq+ 2*p1p4) 
       s15 = (p1sq +p5sq+ 2*p1p5) 
       s23 = (p2sq +p3sq+ 2*p2p3) 
       s24 = (p2sq +p4sq+ 2*p2p4) 
       s25 = (p2sq +p5sq+ 2*p2p5) 
       s34 = (p3sq +p4sq+ 2*p3p4) 
       s35 = (p3sq +p5sq+ 2*p3p5) 
       s45 = (p4sq +p5sq+ 2*p4p5) 
c       Write(*,'(a5,F20.10)')," p1sq ", p1sq 
c       Write(*,'(a5,F20.10)')," p1p2 ", p1p2
c       Write(*,'(a5,F20.10)')," p1p3 ", p1p3
c       Write(*,'(a5,F20.10)')," p1p4 ", p1p4
c       Write(*,'(a5,F20.10)')," p1p5 ", p1p5
c       Write(*,'(a5,F20.10)')," p2sq ", p2sq 
c       Write(*,'(a5,F20.10)')," p2p3 ", p2p3
c       Write(*,'(a5,F20.10)')," p2p4 ", p2p4
c       Write(*,'(a5,F20.10)')," p2p5 ", p2p5
c       Write(*,'(a5,F20.10)')," p3sq ", p3sq 
c       Write(*,'(a5,F20.10)')," p3p4 ", p3p4
c       Write(*,'(a5,F20.10)')," p3p5 ", p3p5
c       Write(*,'(a5,F20.10)')," p4sq ", p4sq 
c       Write(*,'(a5,F20.10)')," p4p5 ", p4p5
c       Write(*,'(a5,F20.10)')," p5sq ", p5sq 
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c    Calling C_ij,D_ij Functions    
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
       if(comp.gt.0)then
       B012=B0tM(m,p1sq)  
       B013=B0tM(m,s12)   
       B014=B0tM(m,s45)   
       B015=B0tM(m,p5sq)  
       B023=B0tM(m,p2sq)  
       B024=B0tM(m,s23)   
       B025=B0tM(m,s15)   
       B034=B0tM(m,p3sq)  
       B035=B0tM(m,s34)   
       B045=B0tM(m,p4sq)  
       B012R=Dble(B012)          
       B013R=Dble(B013) 
       B014R=Dble(B014)   
       B015R=Dble(B015)           
       B023R=Dble(B023 ) 
       B024R=Dble(B024)  
       B025R=Dble(B025)   
       B034R=Dble(B034)  
       B035R=Dble(B035)  
       B045R=Dble(B045)  
       B012I=DIMAG(B012)          
       B013I=DIMAG(B013) 
       B014I=DIMAG(B014)   
       B015I=DIMAG(B015)           
       B023I=DIMAG(B023) 
       B024I=DIMAG(B024)  
       B025I=DIMAG(B025)   
       B034I=DIMAG(B034)  
       B035I=DIMAG(B035)  
       B045I=DIMAG(B045)  
c************************************************************************************
c************************************************************************************
      C0145=C0fin1M(m,s45,p4sq,p5sq) 
      C0125=C0fin1M(m,p1sq,s15,p5sq) 
      C0123=C0fin1M(m,p1sq,p2sq,s12) 
      C0124=C0fin1M(m,p1sq,s23,s45)  
      C0135=C0fin1M(m,s12,s34,p5sq)  
      C0134=C0fin1M(m,s12,p3sq,s45)  
      C0234=C0fin1M(m,p2sq,p3sq,s23) 
      C0235=C0fin1M(m,p2sq,s34,s15)  
      C0245=C0fin1M(m,s23,p4sq,s15)  
      C0345=C0fin1M(m,p3sq,p4sq,s34) 
c************************************************************************************
c************************************************************************************
c************************************************************************************
       call tens_red3_new_Re_Com_1M(m,s45,p4sq,p5sq,B045,B015,B014,C01
     -   45,C0145R,C0145I,C145R,C145I) 
       call tens_red3_new_Re_Com_1M(m,p1sq,s15,p5sq,B025,B015,B012,C01
     -   25,C0125R,C0125I,C125R,C125I) 
       call tens_red3_new_Re_Com_1M(m,p1sq,p2sq,s12,B023,B013,B012,C01
     -   23,C0123R,C0123I,C123R,C123I) 
       call tens_red3_new_Re_Com_1M(m,p1sq,s23,s45,B024,B014,B012,C012
     -   4,C0124R,C0124I,C124R,C124I) 
       call tens_red3_new_Re_Com_1M(m,s12,s34,p5sq,B035,B015,B013,C013
     -   5,C0135R,C0135I,C135R,C135I) 
       call tens_red3_new_Re_Com_1M(m,s12,p3sq,s45,B034,B014,B013,C013
     -   4,C0134R,C0134I,C134R,C134I) 
       call tens_red3_new_Re_Com_1M(m,p2sq,p3sq,s23,B034,B024,B023,C02
     -   34,C0234R,C0234I,C234R,C234I) 
       call tens_red3_new_Re_Com_1M(m,p2sq,s34,s15,B035,B025,B023,C023
     -   5,C0235R,C0235I,C235R,C235I) 
       call tens_red3_new_Re_Com_1M(m,s23,p4sq,s15,B045,B025,B024,C024
     -   5,C0245R,C0245I,C245R,C245I) 
       call tens_red3_new_Re_Com_1M(m,p3sq,p4sq,s34,B045,B035,B034,C03
     -   45,C0345R,C0345I,C345R,C345I) 
c************************************************************************************
c************************************************************************************
       D02345=D04(p2sq,p3sq,p4sq,s15,s23,s34,m,m,m,m)
       D01345=D04(s12,p3sq,p4sq,p5sq,s45,s34,m,m,m,m)
       D01245=D04(p1sq,s23,p4sq,p5sq,s45,s15,m,m,m,m)
       D01235=D04(p1sq,p2sq,s34,p5sq,s12,s15,m,m,m,m)
       D01234=D04(p1sq,p2sq,p3sq,s45,s12,s23,m,m,m,m)
c************************************************************************************
c************************************************************************************
       call tens_red4_new_Re_Com_1M(m,p2sq,p3sq,p4sq,p2p3,p2p4,p3p4,C0
     -   345R,C0245R,C0235R,C0234R,C345R,C245R,C235R,C234R,C0345I,C02
     -   45I,C0235I,C0234I,C345I,C245I,C235I,C234I,D02345,D02345R,D02
     -   345I,D2345R,D2345I)
        call tens_red4_new_Re_Com_1M(m,s12,p3sq,p4sq,p1p3+p2p3,p1p4+p2p
     -   4,p3p4,C0345R,C0145R,C0135R,C0134R,C345R,C145R,C135R,C134R,C
     -   0345I,C0145I,C0135I,C0134I,C345I,C145I,C135I,C134I,D01345,D0
     -   1345R,D01345I,D1345R,D1345I)
        call tens_red4_new_Re_Com_1M(m,p1sq,s23,p4sq,p1p2+p1p3,p1p4,p2p
     -   4+p3p4,C0245R,C0145R,C0125R,C0124R,C245R,C145R,C125R,C124R,C
     -   0245I,C0145I,C0125I,C0124I,C245I,C145I,C125I,C124I,D01245,D0
     -   1245R,D01245I,D1245R,D1245I)
        call tens_red4_new_Re_Com_1M(m,p1sq,p2sq,s34,p1p2,p1p3+p1p4,p2p
     -   3+p2p4,C0235R,C0135R,C0125R,C0123R,C235R,C135R,C125R,C123R,C
     -   0235I,C0135I,C0125I,C0123I,C235I,C135I,C125I,C123I,D01235,D0
     -   1235R,D01235I,D1235R,D1235I)
        call tens_red4_new_Re_Com_1M(m,p1sq,p2sq,p3sq,p1p2,p1p3,p2p3,C0
     -   234R,C0134R,C0124R,C0123R,C234R,C134R,C124R,C123R,C0234I,C01
     -   34I,C0124I,C0123I,C234I,C134I,C124I,C123I,D01234,D01234R,D01
     -   234I,D1234R,D1234I)
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
       E012345=E01M(m,p1sq,p2sq,p3sq,p4sq,p1p2,p1p3,p1p4,p2p3,p2p4,p3p
     -   4,D02345,D01345,D01245,D01235,D01234)
       E012345R=Dble(E012345)   
       E012345I=DIMAG(E012345)
       call tens_red5_new_Re_Com_1M(m,p1sq,p2sq,p3sq,p4sq,p1p2,p1p3,p1
     -   p4,p2p3,p2p4,p3p4,D02345R,D01345R,D01245R,D01235R,D01234R,D2
     -   345R,D1345R,D1245R,D1235R,D1234R,D02345I,D01345I,D01245I,D01
     -   235I,D01234I,D2345I,D1345I,D1245I,D1235I,D1234I,E12345R,E123
     -   45I)
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c       Definition of the F,P functions:Independent of the currents    
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
c************************************************************************************
       P(1) = p5sq-s15+s23-s45
       P(2) = p3sq+p4sq-s34
       P(3) = p4sq-p5sq+s45
       P(4) = -(p3sq*p5sq)-p4sq*s12+p3sq*s15+s12*s15+p5sq*s23-s12*s23-
     -   s23*s34-s15*s45+s34*s45+p1sq*P(2)+p2sq*P(3)
       P(5) = p1sq+p2sq-s12
       FR(1) = 4*C0135R+E012345R*P(4)+2*(D01245R*P(1)-D01345R*P(2)-D01
     -   235R*P(5))
       FI(1) = 4*C0135I+E012345I*P(4)+2*(D01245I*P(1)-D01345I*P(2)-D01
     -   235I*P(5))
       F(1)=DCMPLX(FR(1),FI(1))
       FR(2) = 4*D01235R-2*E012345R*P(2)
       FI(2) = 4*D01235I-2*E012345I*P(2)
       F(2)=DCMPLX(FR(2),FI(2))
       P(6) = p3sq+s15-s23-s34
       P(7) = p2sq+p3sq-s23
       FR(3) = 4*D01245R-2*E012345R*P(7)
       FI(3) = 4*D01245I-2*E012345I*P(7)
       F(3)=DCMPLX(FR(3),FI(3))
       FR(4) = 4*D01345R-2*(8*E12345R(11,2)+E012345R*P(5))
       FI(4) = 4*D01345I-2*(8*E12345I(11,2)+E012345I*P(5))
       F(4)=DCMPLX(FR(4),FI(4))
       FR(5) = -4*D01235R+2*E012345R*P(2)
       FI(5) = -4*D01235I+2*E012345I*P(2)
       F(5)=DCMPLX(FR(5),FI(5))
       FR(6) = -4*D01245R+2*E012345R*P(7)
       FI(6) = -4*D01245I+2*E012345I*P(7)
       F(6)=DCMPLX(FR(6),FI(6))
       P(8) = p2sq-s12-s23+s45
       FR(7) = 2*(-8*E12345R(11,2)+E012345R*P(8))
       FI(7) = 2*(-8*E12345I(11,2)+E012345I*P(8))
       F(7)=DCMPLX(FR(7),FI(7))
       FR(8) = -4*D01345R+2*E012345R*P(5)
       FI(8) = -4*D01345I+2*E012345I*P(5)
       F(8)=DCMPLX(FR(8),FI(8))
       FR(9) = 2*(-8*E12345R(11,2)+E012345R*P(1))
       FI(9) = 2*(-8*E12345I(11,2)+E012345I*P(1))
       F(9)=DCMPLX(FR(9),FI(9))
       FR(10) = 4*D01345R-2*E012345R*P(5)
       FI(10) = 4*D01345I-2*E012345I*P(5)
       F(10)=DCMPLX(FR(10),FI(10))
       FR(11) = 4*D01245R-2*(8*E12345R(11,2)+E012345R*P(7))
       FI(11) = 4*D01245I-2*(8*E12345I(11,2)+E012345I*P(7))
       F(11)=DCMPLX(FR(11),FI(11))
       FR(12) = 2*(-8*E12345R(11,2)+E012345R*P(6))
       FI(12) = 2*(-8*E12345I(11,2)+E012345I*P(6))
       F(12)=DCMPLX(FR(12),FI(12))
       FR(13) = 4*D01235R-2*(8*E12345R(11,2)+E012345R*P(2))
       FI(13) = 4*D01235I-2*(8*E12345I(11,2)+E012345I*P(2))
       F(13)=DCMPLX(FR(13),FI(13))
       FR(14) = -8*(E012345R+E12345R(1,1))
       FI(14) = -8*(E012345I+E12345I(1,1))
       F(14)=DCMPLX(FR(14),FI(14))
       FR(15) = -4*(E012345R+2*E12345R(1,1))
       FI(15) = -4*(E012345I+2*E12345I(1,1))
       F(15)=DCMPLX(FR(15),FI(15))
       FR(16) = 4*(E012345R+2*E12345R(1,1))
       FI(16) = 4*(E012345I+2*E12345I(1,1))
       F(16)=DCMPLX(FR(16),FI(16))
       FR(17) = 8*(E012345R+E12345R(1,1))
       FI(17) = 8*(E012345I+E12345I(1,1))
       F(17)=DCMPLX(FR(17),FI(17))
       P(9) = p1sq-s12
       P(10) = p2sq-p3sq+s23+2*P(9)
       FR(18) = -8*(D01345R+D1345R(1,1))+4*(D01245R+E12345R(1,1)*P(5))
     -   +2*E012345R*P(10)
       FI(18) = -8*(D01345I+D1345I(1,1))+4*(D01245I+E12345I(1,1)*P(5))
     -   +2*E012345I*P(10)
       F(18)=DCMPLX(FR(18),FI(18))
       P(11) = p3sq+s15-s23-s34+2*P(5)
       FR(19) = -8*(D01345R+D1345R(1,1))+4*E12345R(1,1)*P(5)+2*E012345
     -   R*P(11)
       FI(19) = -8*(D01345I+D1345I(1,1))+4*E12345I(1,1)*P(5)+2*E012345
     -   I*P(11)
       F(19)=DCMPLX(FR(19),FI(19))
       FR(20) = -8*(D01245R+D1245R(1,1))+4*(E012345R+E12345R(1,1))*P(7
     -   )
       FI(20) = -8*(D01245I+D1245I(1,1))+4*(E012345I+E12345I(1,1))*P(7
     -   )
       F(20)=DCMPLX(FR(20),FI(20))
       FR(21) = -4*D01245R-8*D1245R(1,1)+2*(E012345R+2*E12345R(1,1))*P
     -   (7)
       FI(21) = -4*D01245I-8*D1245I(1,1)+2*(E012345I+2*E12345I(1,1))*P
     -   (7)
       F(21)=DCMPLX(FR(21),FI(21))
       FR(22) = -8*(D01235R+D1235R(1,1))+4*(E012345R+E12345R(1,1))*P(2
     -   )
       FI(22) = -8*(D01235I+D1235I(1,1))+4*(E012345I+E12345I(1,1))*P(2
     -   )
       F(22)=DCMPLX(FR(22),FI(22))
       FR(23) = -4*D01235R-8*D1235R(1,1)+2*(E012345R+2*E12345R(1,1))*P
     -   (2)
       FI(23) = -4*D01235I-8*D1235I(1,1)+2*(E012345I+2*E12345I(1,1))*P
     -   (2)
       F(23)=DCMPLX(FR(23),FI(23))
       FR(24) = 2*(E012345R+2*E12345R(1,1))*P(6)
       FI(24) = 2*(E012345I+2*E12345I(1,1))*P(6)
       F(24)=DCMPLX(FR(24),FI(24))
